  private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
    // Datos
    AlumnoDto dto = new AlumnoDto();
    dto.setCurso(cboCurso.getSelectedItem().toString());
    dto.setNombre(txtNombre.getText());
    dto.setNota1(Integer.parseInt(txtNota1.getText()));
    dto.setNota2(Integer.parseInt(txtNota2.getText()));
    dto.setNota3(Integer.parseInt(txtNota3.getText()));
    // Proceso
    controller.registrarAlumno(dto);
    // Limpiar
    txtNombre.setText("");
    txtNota1.setText("");
    txtNota2.setText("");
    txtNota3.setText("");
  }//GEN-LAST:event_btnGrabarActionPerformed

