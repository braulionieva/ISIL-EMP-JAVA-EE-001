    private void btn_calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_calcularActionPerformed

        Locale locale = new Locale("en", "UK");
        String pattern = "#,##0.00";

        DecimalFormat df = (DecimalFormat) NumberFormat.getNumberInstance(locale);
        df.applyPattern(pattern);

        // Datos
        double capital = Double.parseDouble(txtCapital.getText());
        double interes = Double.parseDouble(txtInteres.getText());
        double periodo = Double.parseDouble(txtPeriodo.getText());

        // Proceso
        InteresService interesService = new InteresService();
        InteresDto interesDto = new InteresDto();

        interesDto.setCapital(capital);
        interesDto.setTasaInteres(interes);
        interesDto.setPeriodo(periodo);

        InteresDto importeAcumulado = interesService.calculaImporteAcmulado(interesDto);

        txtAcumulado.setText(df.format(importeAcumulado.getImporteAcumulado()));

        // Reporte
        String repo;
        repo = "";
        repo += "Importe Acumuado: " + importeAcumulado.getImporteAcumulado() + "\n";
        JOptionPane.showMessageDialog(rootPane, repo, "REPORTE",
                JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btn_calcularActionPerformed

