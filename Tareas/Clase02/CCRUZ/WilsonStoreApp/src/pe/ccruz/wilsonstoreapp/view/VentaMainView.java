    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // Datos
        VentaDto dto = new VentaDto();
        dto.setCantidad(Integer.parseInt(txtCantidad.getText()));
        dto.setItem(txtDetalle.getText());
        dto.setpUnit(Double.parseDouble(txtPunit.getText()));
        dto.setCodCategoria(cboCategoria.getSelectedItem().toString());
        dto.setCodArticulo(cboArticulo.getSelectedItem().toString());
        
        controlador.registrarVenta(dto);
        
        txtCantidad.setText("");
        txtDetalle.setText("");
        txtPunit.setText("");
        cboCategoria.setSelectedIndex(-1);
        cboArticulo.setSelectedIndex(-1);
        
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        String codCategoria = cboCategoria.getSelectedItem().toString();
        
        List<VentaDto> lista = controlador.getVentasPorCategoria(codCategoria);
        
        
        // Print
        DefaultTableModel tabla;
        tabla = (DefaultTableModel) jTable1.getModel();
        tabla.setRowCount(0);
        
        for (VentaDto dto : lista){
            Object[] rowData = {dto.getCantidad(),dto.getpUnit(),dto.getCodCategoria(), dto.getCodArticulo(), dto.getImporte(), dto.getItem()};
            tabla.addRow(rowData);
        }
    }//GEN-LAST:event_btnMostrarActionPerformed

