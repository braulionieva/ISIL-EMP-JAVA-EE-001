    private void cargarCategorias(){
    cboCategoria.removeAllItems();
    for(String codCategoria: controlador.getCategorias() ){
      cboCategoria.addItem(codCategoria);
    }
    cboCategoria.setSelectedIndex(-1);
  }
    
    private void cargarArticulos(){
    cboArticulo.removeAllItems();
    for(String codArticulo: controlador.getArticulos()){
      cboArticulo.addItem(codArticulo);
    }
    cboArticulo.setSelectedIndex(-1);
  }

