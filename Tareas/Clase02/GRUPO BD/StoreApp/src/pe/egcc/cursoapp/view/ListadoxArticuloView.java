  private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
      // Dato
      String categoria = cboCurso.getSelectedItem().toString();
      TotalVenta = 0;

      if (categoria.equals("TODO")) {
          llenarTablaAllProducto();
      } else {
          // Proceso
          List<ProductoDto> lista = controller.getProductos(categoria);
          // Imprimir
          DefaultTableModel tabla;
          tabla = (DefaultTableModel) tblDatos.getModel();
          tabla.setRowCount(0);

          for (ProductoDto dto : lista) {
              TotalVenta += dto.getPrecio();
              Object[] rowData = {dto.getNum(), dto.getCategoria(), dto.getArticulo(),
                  String.format(Locale.US, "%.2f", dto.getPrecio())//, dto.getPromedio(),dto.isAprobado()
          };
              tabla.addRow(rowData);
          }
          //Cantidad total de venta
          lblTotalVenta.setText("TOTAL DE VENTA : " + String.format(Locale.US, "%.2f", TotalVenta));
          //Cantidad de filas de la tabla
          lblCant.setText("CANTIDAD : " + tblDatos.getModel().getRowCount());
      }
  }//GEN-LAST:event_btnConsultarActionPerformed

