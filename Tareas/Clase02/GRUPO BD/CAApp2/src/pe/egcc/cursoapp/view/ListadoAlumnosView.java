  private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
      // Dato
      String curso = cboCurso.getSelectedItem().toString();
      // Proceso
      List<AlumnoDto> lista = controller.getAlumnos(curso);
      // Imprimir
      DefaultTableModel tabla;
      tabla = (DefaultTableModel) tblDatos.getModel();
      tabla.setRowCount(0);
      // tblDatos.removeColumn(tblDatos.getColumn(2)); // REMOVE

      for (AlumnoDto dto : lista) {
          Object[] rowData = {dto.getNumIncid(), dto.getNombre(), dto.getResumen(), dto.getFecha()//, dto.getPromedio(),dto.isAprobado()
          };
          tabla.addRow(rowData);
      }
      //Cantidad de filas de la tabla
      lblCant.setText("CANTIDAD : " + tblDatos.getModel().getRowCount());
      //  lblCant.setText(tblDatos.getColumnModel().getColumnCount()+"");

  }//GEN-LAST:event_btnConsultarActionPerformed

