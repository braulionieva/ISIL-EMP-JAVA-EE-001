    private void cbxNombreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxNombreActionPerformed
        
        switch(cbxNombre.getSelectedIndex()){
            case 0:  txtcodigo.setText("100");
                     txaDetalle.setText("Se pueden resolver durante la semana.");
                     break;
            case 1:  txtcodigo.setText("200");//
                     txaDetalle.setText("Se deben resolver en el transcurso \n del día.");
                     break;
            case 2:  txtcodigo.setText("300");
                     txaDetalle.setText("Necesita ser resuelta lo antes posible, \n incluso antes que lo cotidiano y\n" +
                                        "durante el día.");
                     break;
            case 3:  txtcodigo.setText("400");
                     txaDetalle.setText("Necesita ser resuelta ahora mismo.");
                     break;
            case 4:  txtcodigo.setText("500");
                     txaDetalle.setText("Es la prioridad mas alta, \n y necesita ser resuelta ahora mismo,\n incluso " +
                                        "dejando de hacer las \n incidencias urgentes.");
                     break;         
            default : txtcodigo.setText("");
                      txaDetalle.setText("");
                      txaIncidente.setText("");
                     break;
            
        }
    }//GEN-LAST:event_cbxNombreActionPerformed

