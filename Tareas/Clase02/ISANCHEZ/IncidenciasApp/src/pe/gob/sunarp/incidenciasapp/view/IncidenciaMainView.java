    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        IncidenciaDto dto = new IncidenciaDto();
        dto.setNombre(cbxNombre.getSelectedItem().toString());
        dto.setCodigo(Integer.parseInt(txtcodigo.getText()));
        dto.setDetalle(txaDetalle.getText());
        dto.setIncidente(txaIncidente.getText());
        
        controlador.registrarIncidencia(dto);
        
        txtcodigo.setText("");
        txaDetalle.setText("");
        txaIncidente.setText("");
        cbxNombre.setSelectedIndex(-1);
    }//GEN-LAST:event_btnGrabarActionPerformed

